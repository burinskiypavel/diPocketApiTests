package com.cs.dipocketback.pojo.peak;

public class PinRevealResponse {
    
    private String pin;
    
    public PinRevealResponse() {
    }

    public PinRevealResponse(String pin) {
        this.pin = pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getPin() {
        return pin;
    }

}
