package com.cs.dipocketback.pojo.references.params;

public class AppParamLinks {
    
    private String contactUs;
    private String faq;
    private String privacyPolicy;
    private String vendorSite;
    private String legal;
    
    public AppParamLinks() {        
    }

    public void setContactUs(String contactUs) {
        this.contactUs = contactUs;
    }

    public String getContactUs() {
        return contactUs;
    }

    public void setFaq(String faq) {
        this.faq = faq;
    }

    public String getFaq() {
        return faq;
    }

    public void setPrivacyPolicy(String privacyPolicy) {
        this.privacyPolicy = privacyPolicy;
    }

    public String getPrivacyPolicy() {
        return privacyPolicy;
    }

    public void setVendorSite(String vendorSite) {
        this.vendorSite = vendorSite;
    }

    public String getVendorSite() {
        return vendorSite;
    }

    public void setLegal(String legal) {
        this.legal = legal;
    }

    public String getLegal() {
        return legal;
    }

}
