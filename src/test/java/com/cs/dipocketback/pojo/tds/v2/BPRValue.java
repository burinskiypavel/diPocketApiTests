package com.cs.dipocketback.pojo.tds.v2;

public class BPRValue {

    private Long id;
    private String bprName;
    private String bprValue;

    public BPRValue() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBprName() {
        return bprName;
    }

    public void setBprName(String bprName) {
        this.bprName = bprName;
    }

    public String getBprValue() {
        return bprValue;
    }

    public void setBprValue(String bprValue) {
        this.bprValue = bprValue;
    }

}
